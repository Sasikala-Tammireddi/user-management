import { NgModule } from '@angular/core';
import { PreloadAllModules, RouterModule, Routes } from '@angular/router';

const routes: Routes = [
  {
    path: '',
    redirectTo: 'login',
    pathMatch: 'full'
  },
  {
    path: 'folder/:id',
    loadChildren: () => import('./folder/folder.module').then( m => m.FolderPageModule)
  },
  {
    path: 'http',
    loadChildren: () => import('./pages/http/http.module').then( m => m.HttpPageModule)
  },
  {
    path: 'firebase',
    loadChildren: () => import('./pages/firebase/firebase.module').then( m => m.FirebasePageModule)
  },
  {
    path: 'sqlite',
    loadChildren: () => import('./pages/sqlite/sqlite.module').then( m => m.SqlitePageModule)
  },
  {
    path: 'login',
    loadChildren: () => import('./pages/login/login.module').then( m => m.LoginPageModule)
  },
  {
    path: 'register',
    loadChildren: () => import('./pages/register/register.module').then( m => m.RegisterPageModule)
  }
];

@NgModule({
  imports: [
    RouterModule.forRoot(routes, { preloadingStrategy: PreloadAllModules })
  ],
  exports: [RouterModule]
})
export class AppRoutingModule {}
